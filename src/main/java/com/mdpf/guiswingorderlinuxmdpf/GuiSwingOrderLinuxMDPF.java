    private void btncalculatorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalculatorActionPerformed
        // TODO add your handling code here:
        String os = System.getProperty("os.name").toLowerCase();

        try {
            setLocationRelativeTo(null);
            if (os.contains("win")) {
                new ProcessBuilder("calc").start();
            } else if (os.contains("linux")) {
                new ProcessBuilder("kcalc").start();
            } else {
                JOptionPane.showMessageDialog(this, "Calculator is not supported on your operating system.", "Unsupported OS", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "An error occurred while trying to open the calculator.", "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }

    }//GEN-LAST:event_btncalculatorActionPerformed

