    private void btnvieworderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnvieworderActionPerformed
        // TODO add your handling code here:
        if (TakeOrder.orders.isEmpty()) {
            JOptionPane.showMessageDialog(this, "There are no orders", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String[] customerNames = new String[TakeOrder.orders.size()];
        for (int i = 0; i < TakeOrder.orders.size(); i++) {
            customerNames[i] = TakeOrder.orders.get(i).getFullCustomerName();
        }

        String selectedCustomerName = (String) JOptionPane.showInputDialog(
                this,
                "Choose the order that you want to see:",
                "Choose order",
                JOptionPane.QUESTION_MESSAGE,
                null,
                customerNames,
                customerNames[0]
        );

        if (selectedCustomerName != null) {
            Order selectedOrder = null;
            for (Order order : TakeOrder.orders) {
                if (order.getFullCustomerName().equals(selectedCustomerName)) {
                    selectedOrder = order;
                    break;
                }
            }

            if (selectedOrder != null) {
                JOptionPane.showMessageDialog(
                        this,
                        "Order details:\n" + selectedOrder.toString(),
                        "Selected order",
                        JOptionPane.INFORMATION_MESSAGE
                );
            }
        }
    }//GEN-LAST:event_btnvieworderActionPerformed

